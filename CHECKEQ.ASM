PRINT MACRO MES
MOV AH,09H
LEA DX,MES
INT 21H
ENDM

DATA SEGMENT                      
MSG1 DB 10, 13, "ENTER NUMBER1:$"
MSG2 DB 10, 13, "ENTER NUMBER2:$"
MSG3 DB 10, 13, "UNEQUAL.$"
MSG4 DB 10, 13, "EQUAL.$"
SBUFF1 DB 25, ?, 25 DUP('$')
SBUFF2 DB 25, ?, 25 DUP('$')       
DATA ENDS

CODE SEGMENT
ASSUME DS:DATA, CS:CODE
START:
MOV AX, DATA
MOV DS, AX

PRINT MSG1
MOV AH, 0AH
LEA DX, SBUFF1
INT 21H

PRINT MSG2
LEA DX,SBUFF2
MOV AH,0AH
INT 21H

LEA SI, SBUFF1
LEA DI, SBUFF2
MOV CL, LENGTH SBUFF1
MOV CH, LENGTH SBUFF2
CMP CH,CL
JNE UNEQUAL
MOV CH, 00H
INC SI
INC SI
INC DI
INC DI
LOOPP:
MOV BL, [SI]
CMP BL, [DI]
JNE UNEQUAL
INC SI
INC DI
LOOP LOOPP

EQUAL:
PRINT MSG4
JMP OVER

UNEQUAL:
PRINT MSG3

OVER:
INT 03H
CODE ENDS
END START 