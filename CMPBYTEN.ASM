DATA SEGMENT
X DB 0CH
MSG1 DB "EQUAL TO 10.","$"
MSG2 DB "GREATER THAN 10.","$"
MSG3 DB "LESS THAN 10.","$"
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA

START:
MOV AX,DATA
MOV DS,AX

MOV AL,X
MOV AH,00H
CMP AL,0AH
JZ EQUAL
JS LESSER
GREATER:
LEA DX,MSG2
MOV AH,09H 
INT 21H
JMP OVER
EQUAL:
LEA DX,MSG1
MOV AH,09H 
INT 21H  
JMP OVER  
LESSER:
LEA DX,MSG3
MOV AH,09H 
INT 21H

OVER:
INT 03H
CODE ENDS
END START