DATA SEGMENT
X DB ?
MSG1 DB 10,13,"IS PRIME.$"
MSG2 DB 10,13,"IS NOT PRIME.$"
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA

START:
MOV AX, DATA
MOV DS, AX

MOV AX,00H

MOV CL,02H
LOOPP:
MOV AH,00H
MOV AL,X
DIV CL
CMP AH, 00H
JZ NOTP
INC CL
CMP CL,X
JNZ LOOPP

PRIME:
LEA DX,MSG1
MOV AH,09H
INT 21H
JMP OVER

NOTP:
LEA DX,MSG2
MOV AH,09H
INT 21H

OVER:
INT 03H
CODE ENDS
END START