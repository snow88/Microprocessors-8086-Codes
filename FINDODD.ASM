DATA SEGMENT
ARRAY DB 1,4,1,4,4,5,9,4,9 ; ONLY 5 OCCURS ODD NO. OF TIMES
MSG DB " IS REPEATED ODD NO. OF TIMES.$"
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA

START:
MOV AX,DATA
MOV DS,AX

MOV AX,00H
MOV DH,09H   ; NO. OF ELEMENTS IN ARRAY
LEA SI,ARRAY
MYLOOP:
MOV DL,09H
MOV BL,[SI]
MOV CX,00H   ; TRACKS NO. OF OCCURENCES
LEA DI,ARRAY
MYLOOP2:
MOV AL,[DI]
XOR AL,BL 
JNZ SKIP
INC CX
SKIP:
INC DI
DEC DL
JNZ MYLOOP2

CHECKODD:
AND CX,01H
JNZ OUTPUT

INC SI
DEC DH
JNZ MYLOOP
JMP OVER

OUTPUT:
ADD BL,48 
MOV AH, 02H  
MOV DL, BL
INT 21H
LEA DX,MSG
MOV AH,09H
INT 21H

OVER:
INT 03H
CODE ENDS
END START