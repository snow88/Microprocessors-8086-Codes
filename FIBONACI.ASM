DATA SEGMENT
FIB DB 10 DUP(?)
DATA ENDS

CODE SEGMENT
ASSUME DS:DATA, CS:CODE
START:
MOV AX, DATA
MOV DS, AX

MOV CL, 08H
LEA SI, FIB
MOV AL, 00H
MOV [SI], AL
INC SI
MOV AL, 01H
MOV [SI],AL
INC SI
FLOOP:
MOV AL, [SI-2]
MOV BL, [SI-1]
ADD AL,BL
MOV [SI],AL
INC SI
DEC CL
JNZ FLOOP

CALL PRINTARR
INT 03H

PRINTARR PROC     ;HEX PRINT
MOV CH, LENGTH FIB
LEA DI,FIB
PLOOP:
ASCIIL:
MOV DL,[DI]
AND DL,00001111B
CMP DL,0AH
JC SKIPAR1
ADD DL,07H
SKIPAR1:
ADD DL,30H
MOV AH,09H
INT 21H
ASCIIH:
MOV DL,[DI]
ROR DL,01H
ROR DL,01H
ROR DL,01H
ROR DL,01H
AND DL,00001111B
CMP DL,0AH
JC SKIPAR2
ADD DL,07H
SKIPAR2:
ADD DL,30H
MOV AH,09H
INT 21H
SPACE:
MOV DL, ' '
MOV AH,09H
INT 21H
INC DI
DEC CH
JNZ PLOOP
RET
PRINTARR ENDP

CODE ENDS
END START 



