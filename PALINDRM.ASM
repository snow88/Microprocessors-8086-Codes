DATA SEGMENT                      
MSG1 DB 10, 13, "ENTER STRING:$"
MSG2 DB 10, 13, "PALINDROME.$"
MSG3 DB 10, 13, "NOT PALINDROME.$"
SBUFF1 DB 25, ?, 25 DUP('$')      
DATA ENDS

CODE SEGMENT
ASSUME DS:DATA, CS:CODE
START:
MOV AX, DATA
MOV DS, AX

PRINT MSG1

MOV AH, 0AH
LEA DX, SBUFF1
INT 21H

LEA SI, SBUFF1
MOV CH, 00H
MOV CL, LENGTH SBUFF1
MOV AX, SI
ADD AX, CX
DEC AX
MOV DI, AX 

