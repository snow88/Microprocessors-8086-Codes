DATA SEGMENT
ARRAY DB 21H,55H,61H,23H,41H,53H,23H,56H,12H,54H  ; ARRAY OF 10 NUMBERS
X DB 53H  ;  NUMBER TO SEARCH FOR
MSG1 DB "FOUND$"
MSG2 DB "NOT FOUND$"
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA

START:
MOV AX,DATA
MOV DS,AX

MOV AX,00H
MOV CX,0AH ; COUNTER FOR 10 NUMBERS
MOV BL,X
LEA SI,ARRAY
MYLOOP:
MOV AL,[SI]
CMP AL,BL
JZ FOUND
INC SI
DEC CX
JNZ MYLOOP
NOTFOUND:
LEA DX,MSG2
MOV AH,09H
INT 21H
JMP OVER
FOUND:
LEA DX,MSG1
MOV AH,09H
INT 21H
OVER:
INT 03H
CODE ENDS
END START