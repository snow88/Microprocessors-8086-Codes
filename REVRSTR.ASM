.MODEL SMALL
.STACK 256

.DATA
REV DB 40 DUP(?)
SBUFF1 DB 25, ?, 25 DUP('$') 
MSG1 DB 10, 13, "ENTER STRING:$"

.CODE
MOV AX, @DATA
MOV DS, AX

LEA DX,MSG1
MOV AH,09H
INT 21H

LEA DX,SBUFF1
MOV AH,0AH
INT 21H

LEA SI, SBUFF1
MOV CH, 00H
MOV CL, LENGTH SBUFF1
MOV AX, SI
ADD AX, CX
DEC AX
MOV SI, AX

LEA DI, REV

MOV AH, 00H
LOOPR:
MOV AL, [SI]
MOV [DI], AL
INC DI
DEC SI
LOOP LOOPR

LEA DX, REV
MOV AH, 09H
INT 21H

INT 03H
END