DATA SEGMENT
ARRAY DB 30 DUP(?)   ; ARRAY (30 IN DECIMAL HERE)
MSG1 DB "ENTER NUMBER OF ELEMENTS IN ARRAY (1-9) : $"
MSG2 DB "ENTER ELEMENTS OF THE ARRAY : $"
MSG3 DB "ENTER NUMBER TO SEARCH FOR : $"
MSG4 DB "FOUND$"
MSG5 DB "NOT FOUND$"
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA

START:
MOV AX,DATA
MOV DS,AX

LEA DX,MSG1
MOV AH,09H
INT 21H

MOV AH,01H
INT 21H
AND AL,0FH  ;  CONVERT ASCII TO DECIMAL
MOV BL,AL  ;  STORE INPUT NO. OF ELEMENTS IN BL

LEA DX,MSG2
MOV AH,09H
INT 21H

MOV CL,BL   ;  INPUT ARRAY
LEA SI,ARRAY
MYLOOP:
MOV AH,01H
INT 21H
AND AL,0FH 
MOV [SI],AL
INC SI
DEC CL
JNZ MYLOOP

LEA DX,MSG3
MOV AH,09H
INT 21H

MOV AH,01H   ;  STORE NO. TO SEARCH IN AL
INT 21H
AND AL,0FH 

SEARCH:
LEA SI,ARRAY
MYLOOP2:
MOV CL,[SI]
CMP AL,CL
JZ FOUND
INC SI
DEC BL
JNZ MYLOOP2

NOTFOUND:
LEA DX,MSG5
MOV AH,09H
INT 21H
JMP OVER

FOUND:
LEA DX,MSG4
MOV AH,09H
INT 21H

OVER:
INT 03H
CODE ENDS
END START